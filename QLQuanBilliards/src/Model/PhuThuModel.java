
package Model;

public class PhuThuModel {
    private String MaPT;
    private String vande;
    private String nguyennhan;
    private String xuli;
    private double chiphi;

    public String getMaPT() {
        return MaPT;
    }

    public void setMaPT(String MaPT) {
        this.MaPT = MaPT;
    }

    public String getVande() {
        return vande;
    }

    public void setVande(String vande) {
        this.vande = vande;
    }

    public String getNguyennhan() {
        return nguyennhan;
    }

    public void setNguyennhan(String nguyennhan) {
        this.nguyennhan = nguyennhan;
    }

    public String getXuli() {
        return xuli;
    }

    public void setXuli(String xuli) {
        this.xuli = xuli;
    }

    public double getChiphi() {
        return chiphi;
    }

    public void setChiphi(double chiphi) {
        this.chiphi = chiphi;
    }
    
}
